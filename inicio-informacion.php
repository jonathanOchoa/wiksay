				 <h2>Qué es una cookie?</h2>

				<p>Una cookie es un pequeño fichero de datos que se descarga del servidor de Internet en cualquier dispositivo del usuario, sea un ordenador, un Smartphone o una Tablet,
				con el objetivo de almacenar y recuperar la información sobre la navegación del usuario que se efectúe desde su equipo.</p>

				<p>Las cookies se asocian únicamente al navegador del usuario y no proporcionan por sí mismas datos personales.</p>

				<p>Las cookies no pueden dañar el dispositivo del usuario y, además, son muy útiles, pues ayudan a identificar y a resolver errores, aportando ventajas en la prestación 
				de servicios interactivos, como la mejora de los servicios ofrecidos o la adecuación del contenido de la website de Wiksay.com a las preferencias del usuario.</p>

				<h2>Tipos de cookies:</h2>

				<p>– Cookies propias: son aquéllas que envían al equipo terminal del usuario desde un equipo o dominio gestionado por Wiksay.com y desde el que se presta el servicio 
				solicitado por el usuario. Por tanto, están habilitadas por el sitio web por el que el usuario navega.</p>

				<p>– Cookies de tercero: son aquéllas que se envían al equipo terminal del usuario desde un equipo o dominio que no es gestionado por Wiksay.com, sino por otra entidad 
				que trata los datos obtenidos a través de las cookies. Por tanto, proceden de otros sitios web distintos a Wiksay.com.</p>

				<p>– Cookies de sesión: son un tipo de cookies diseñadas para recabar y almacenar datos mientras el usuario accede a la website de Wiksay.com. Caducan al finalizar la 
				navegación del usuario en la website de Wiksay.com.</p>

				<p>– Cookies persistentes: son un tipo de cookies en el que los datos siguen almacenados en el terminal y pueden ser accedidos y tratados durante un periodo definido por 
				el responsable de la cookie, es decir, por Wiksay.com. No caducan al finalizar la navegación del usuario en la website de Wiksay.com.</p>

				<p>– Cookies técnicas: son aquéllas que permiten al usuario la navegación a través de una página web, plataforma o aplicación y la utilización de las diferentes opciones 
				o servicios que en ella existan.</p>

				<p>– Cookies de análisis: son aquéllas que permiten al responsable de las mismas el seguimiento y análisis del comportamiento de los usuarios de los sitios web a los que 
				están vinculadas. La información recogida mediante este tipo de cookies se utiliza en la medición de la actividad de los sitios web, aplicación o plataforma y para la 
				elaboración de perfiles de navegación de los usuarios de dichos sitios, aplicaciones y plataformas, con el fin de introducir mejoras en función del análisis de los datos 
				de uso que hacen los usuarios del servicio.</p>

				<p>– Cookies publicitarias: son aquéllas que permiten la gestión, de forma más eficaz posible, de los espacios publicitarios que, en su caso, Wiksay.com haya incluido 
				en una página web, aplicación o plataforma desde la que presta el servicio solicitado en base a criterios como el contenido editado o la frecuencia en la que se muestran
				los anuncios.</p>

