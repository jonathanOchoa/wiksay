				<p>– Cookies técnicas: son aquéllas que permiten al usuario la navegación a través de una página web, plataforma o aplicación y la utilización de las diferentes opciones 
				o servicios que en ella existan.</p>

				<p>– Cookies de análisis: son aquéllas que permiten al responsable de las mismas el seguimiento y análisis del comportamiento de los usuarios de los sitios web a los que 
